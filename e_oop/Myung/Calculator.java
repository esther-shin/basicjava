package e_oop;

public class Calculator {
	
	//두 값의 산술연산을 수행하는 메서드들을 만들어주세요.
	
	double add(double a,double b){
		return a+b;
	}
	
	double minus(double a, double b){
		return a-b;
	}
	
	double multi(double a, double b){
		return a*b;
	}
	
	double divide(double a, double b){
		return a/b;
	}
	
	double nan(double a, double b){
		return a%b;
	}
}
